    void ActualizarComboBox() {
        //Agregar los ID a los ComboBox
        try{
            Connection con=DriverManager.getConnection(url,"root","");
            PreparedStatement ps=con.prepareStatement("SELECT ID FROM `datosalumnos`;");
            ResultSet rs=ps.executeQuery();
            
            BajaCB.removeAllItems();
            BajaCB.addItem(null);
            
            
            while(rs.next()){
                Integer id=rs.getInt("ID");
                BajaCB.addItem(id.toString());
            }
            
            
            con.close();
            ps.close();
            
        }catch(Exception e){
            
        }
        
    }

